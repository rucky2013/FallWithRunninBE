package com.tamfign.model;

public class User {
	private int _id;
	private int _state;
	private String _name;

	public int getId() {
		return _id;
	}

	public void setId(int id) {
		this._id = id;
	}

	public int getState() {
		return _state;
	}

	public void setState(int state) {
		this._state = state;
	}

	public String getName() {
		return _name;
	}

	public void setName(String name) {
		this._name = name;
	}
}
